#The values that evaluate to True are known as truthy values. Similarly, values that evaluate to False are called falsy values.
# In Python these values are considered falsy.
#     None
#     False
#     0
#     0.0
#     empty strings, lists, dictionaries etc

#     truthy values include 1 or any non-zero number, non-empty strings, lists, dictionaries etc.

x = -5
if x:
    print("x is truthy")
else:
    print("x is falsy")
y = 0
if y:
    print("y is truthy")
else:
    print("y is falsy")
